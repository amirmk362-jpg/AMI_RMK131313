var PRODUCTS_DB = {};
Object.keys(RAW).forEach(function (id) {
  var r = RAW[id];
  var name = r[0],
    price = r[1],
    old = r[2],
    disc = r[3],
    cat = r[4],
    subcat = r[5];
  var colorsArr = r[6].slice(),
    related = r[7];
  var mi = colorsArr.findIndex(function (c) {
    return name.indexOf(c) !== -1;
  });
  if (mi > 0) {
    var m = colorsArr.splice(mi, 1)[0];
    colorsArr.unshift(m);
  }
  var colors = {};
  colorsArr.forEach(function (c) {
    colors[c] = genImages(cat, c, subcat);
  });
  PRODUCTS_DB[id] = {
    name: name,
    price: price,
    old: old,
    disc: disc,
    cat: cat,
    subcat: subcat,
    colors: colors,
    related: related,
  };
});
