/* ===== SVG داخلی — فقط برای حالت Fallback (وقتی عکس اصلی لود نشه) ===== */
function svgImg(w, h, bg, fg, line1, line2) {
  var fs1 = Math.round(w * 0.09),
    fs2 = Math.round(w * 0.045);
  var svg =
    '<svg xmlns="http://www.w3.org/2000/svg" width="' +
    w +
    '" height="' +
    h +
    '" viewBox="0 0 ' +
    w +
    " " +
    h +
    '">' +
    '<rect width="' +
    w +
    '" height="' +
    h +
    '" fill="#' +
    bg +
    '"/>' +
    '<text x="50%" y="' +
    (line2 ? "45%" : "50%") +
    '" font-family="Tahoma,Arial,sans-serif" font-size="' +
    fs1 +
    '" font-weight="bold" fill="#' +
    fg +
    '" text-anchor="middle" dominant-baseline="middle">' +
    line1 +
    "</text>";
  if (line2)
    svg +=
      '<text x="50%" y="58%" font-family="Tahoma,Arial,sans-serif" font-size="' +
      fs2 +
      '" fill="#' +
      fg +
      '" text-anchor="middle" dominant-baseline="middle" opacity="0.85">' +
      line2 +
      "</text>";
  svg += "</svg>";
  return "data:image/svg+xml," + encodeURIComponent(svg);
}
/* اگه عکس واقعی لود نشد، این جایگزینش می‌شه */
function imgFallback(el, color) {
  if (el.dataset.fb) return;
  el.dataset.fb = "1";
  var hx = COLOR_HEX[color] || ["cccccc", "333333"];
  el.src = svgImg(700, 700, hx[0], hx[1], "AMI RMK13", color);
}
function setImgSrc(el, src, color) {
  el.dataset.fb = "";
  el.onerror = function () {
    imgFallback(this, color);
  };
  el.src = src;
}

